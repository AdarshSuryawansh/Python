#Functions in List

list1=[1,23,34,45,56,67,78]
print(list1)

list1.append(89) #add element at the end.
print(list1)

list1.insert(4,50) #it insert element at the specific position or index.
print(list1)

list1.pop() #it remove or delete last element from the list.
print(list1)

list1.remove(50) #it remove or delete provided element from the list.
print(list1)

list1.pop(4) #delete specific index element from the list
print(list1)
